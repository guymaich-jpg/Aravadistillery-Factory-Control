let currentLang = localStorage.getItem('factory_lang') || 'he';

function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || (I18N.en[key]) || key;
}

// Always return Hebrew translation (for Google Sheets sync)
function tHe(key) {
  return I18N.he[key] || I18N.en[key] || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('factory_lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'he' ? 'rtl' : 'ltr';
  // Re-render current view
  if (typeof renderApp === 'function') renderApp();
}

function toggleLang() {
  // Toggle between Hebrew and Thai
  setLang(currentLang === 'he' ? 'th' : 'he');
}
